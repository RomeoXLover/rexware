export interface RedeemKeyRow {
  id: string;
  code: string;
  type: "subscription" | "plugin";
  plan_id: string | null;
  plugin_id: string | null;
  duration_days: number | null;
  note: string | null;
  redeemed_by: string | null;
  redeemed_at: number | null;
  created_by: string;
  created_at: number;
}

function generateKeyCode(): string {
  const alphabet = "ABCDEFGHJKMNPQRSTUVWXYZ23456789";
  const group = () =>
    Array.from(
      { length: 4 },
      () => alphabet[Math.floor(Math.random() * alphabet.length)],
    ).join("");
  return `${group()}-${group()}-${group()}-${group()}`;
}

export const keysRepo = {
  async create(input: {
    type: "subscription" | "plugin";
    planId?: string | null;
    pluginId?: string | null;
    durationDays?: number | null;
    note?: string | null;
    createdBy: string;
  }): Promise<RedeemKeyRow> {
    const id = newId("key");
    // Retry a few times in the (astronomically unlikely) event of a collision.
    let code = generateKeyCode();
    for (let i = 0; i < 5; i++) {
      const exists = await queryOne("SELECT 1 FROM redeem_keys WHERE code = ?", [code]);
      if (!exists) break;
      code = generateKeyCode();
    }
    await execute(`INSERT INTO redeem_keys
         (id, code, type, plan_id, plugin_id, duration_days, note, created_by, created_at)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)`, [id,
      code,
      input.type,
      input.planId ?? null,
      input.pluginId ?? null,
      input.durationDays ?? null,
      input.note ?? null,
      input.createdBy,
      now()]);
    return await queryOne("SELECT * FROM redeem_keys WHERE id = ?", [id]) as RedeemKeyRow;
  },

  async byCode(code: string): Promise<RedeemKeyRow | undefined> {
    return await queryOne("SELECT * FROM redeem_keys WHERE code = ?", [code]) as RedeemKeyRow | undefined;
  },

  async markRedeemed(id: string, userId: string): Promise<void> {
    await execute("UPDATE redeem_keys SET redeemed_by = ?, redeemed_at = ? WHERE id = ?", [userId, now(), id]);
  },

  async list(): Promise<RedeemKeyRow[]> {
    return await query("SELECT * FROM redeem_keys ORDER BY created_at DESC", []) as RedeemKeyRow[];
  },

  async delete(id: string): Promise<void> {
    await execute("DELETE FROM redeem_keys WHERE id = ?", [id]);
  },
};

