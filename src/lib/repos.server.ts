export const paymentsRepo = {
  async create(input: {
    id: string;
    userId: string;
    planId?: string | null;
    pluginId?: string | null;
    kind?: "subscription" | "plugin" | "slot" | "bot_hours";
    subscriptionId?: string | null;
    coin: string;
    amountUsd: number;
    creditApplied?: number;
    slotQty?: number;
  }): Promise<PaymentRow> {
    const t = now();
    await execute(`INSERT INTO payments (id, user_id, plan_id, plugin_id, kind, subscription_id, coin, amount_usd, credit_applied, slot_qty, status, created_at, updated_at)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 'waiting', ?, ?)`, [
      input.id,
      input.userId,
      input.planId ?? null,
      input.pluginId ?? null,
      input.kind ?? "subscription",
      input.subscriptionId ?? null,
      input.coin,
      input.amountUsd,
      input.creditApplied ?? 0,
      input.slotQty ?? 0,
      t,
      t,
    ]);
    return await queryOne("SELECT * FROM payments WHERE id = ?", [input.id]) as
      PaymentRow;
  },

  async setAddress(id: string, address: string, amountCrypto: string): Promise<void> {
    await execute("UPDATE payments SET pay_address = ?, amount_crypto = ?, updated_at = ? WHERE id = ?", [address, amountCrypto, now(), id]);
  },

  async updateStatus(
    id: string,
    status: PaymentRow["status"],
    extra?: { receivedCrypto?: string; txid?: string },
  ): Promise<void> {
    await execute(`UPDATE payments SET status = ?,
           received_crypto = COALESCE(?, received_crypto),
           txid = COALESCE(?, txid),
           updated_at = ?
         WHERE id = ?`, [status, extra?.receivedCrypto ?? null, extra?.txid ?? null, now(), id]);
  },

  async byId(id: string): Promise<PaymentRow | undefined> {
    return await queryOne("SELECT * FROM payments WHERE id = ?", [id]) as
      | PaymentRow
      | undefined;
  },

  async byIdForUser(id: string, userId: string): Promise<PaymentRow | undefined> {
    return await queryOne("SELECT * FROM payments WHERE id = ? AND user_id = ?", [id, userId]) as PaymentRow | undefined;
  },

  async pendingForUser(userId: string): Promise<PaymentRow | undefined> {
    return await queryOne(`SELECT * FROM payments WHERE user_id = ? AND status IN ('waiting', 'confirming')
         ORDER BY created_at DESC LIMIT 1`, [userId]) as PaymentRow | undefined;
  },

  async listForUser(userId: string): Promise<PaymentRow[]> {
    return await query("SELECT * FROM payments WHERE user_id = ? ORDER BY created_at DESC", [userId]) as PaymentRow[];
  },

  async recent(limit = 50): Promise<PaymentRow[]> {
    return await query("SELECT * FROM payments ORDER BY created_at DESC LIMIT ?", [limit]) as PaymentRow[];
  },

  /** Paid invoices the bot hasn't DM'd the player about yet. Bot polls this. */
  async pendingBotNotification(limit = 20): Promise<PaymentRow[]> {
    return await query(
      `SELECT * FROM payments WHERE status = 'paid' AND notified_at IS NULL ORDER BY updated_at ASC LIMIT ?`,
      [limit],
    ) as PaymentRow[];
  },

  /** Mark a paid invoice as notified so the bot won't spam the player. */
  async markNotified(id: string): Promise<void> {
    await execute("UPDATE payments SET notified_at = ? WHERE id = ? AND notified_at IS NULL", [now(), id]);
  },

  async totalRevenueUsd(): Promise<number> {
    return (
      (await queryOne("SELECT COALESCE(SUM(amount_usd), 0) AS s FROM payments WHERE status = 'paid'", []) as { s: number }).s
    );
  },

  async cancelWaiting(id: string, userId: string): Promise<boolean> {
    // Only cancel if status is 'waiting' (not yet confirmed/paid)
    const result = await execute(`UPDATE payments SET status = 'failed', updated_at = ?
         WHERE id = ? AND user_id = ? AND status = 'waiting'`, [now(), id, userId]);
    if (result.rowCount > 0) await paymentsRepo.refundCredit(id);
    return result.rowCount > 0;
  },

  async adminCancel(id: string): Promise<boolean> {
    const result = await execute(`UPDATE payments SET status = 'failed', updated_at = ?
         WHERE id = ? AND status IN ('waiting', 'confirming')`, [now(), id]);
    if (result.rowCount > 0) await paymentsRepo.refundCredit(id);
    return result.rowCount > 0;
  },

  /**
   * Return any spent referral credit to the user's balance and zero it on the
   * payment, so cancelling an invoice never burns the buyer's credit. Idempotent.
   */
  async refundCredit(id: string): Promise<void> {
    const p = (await queryOne(
      "SELECT user_id, credit_applied FROM payments WHERE id = ?",
      [id],
    )) as { user_id: string; credit_applied: number } | undefined;
    if (!p || !p.credit_applied) return;
    await usersRepo.addCredit(p.user_id, p.credit_applied);
    await execute("UPDATE payments SET credit_applied = 0 WHERE id = ?", [id]);
  },
};

