export const usersRepo = {
  async upsertOnLogin(input: {
  id: string;
  username: string;
  globalName: string | null;
  avatarUrl: string | null;
  email: string | null;  // ✅ Aggiungi email
}): Promise<UserRow> {
  const existing = await queryOne("SELECT * FROM users WHERE id = ?", [input.id]) as UserRow | undefined;

  if (existing) {
    await execute(`UPDATE users SET username = ?, global_name = ?, avatar_url = ?, email = ?, last_login_at = ?
       WHERE id = ?`, [input.username,
      input.globalName,
      input.avatarUrl,
      input.email,  // ✅ Aggiungi email
      now(),
      input.id]);
  } else {
    await execute(`INSERT INTO users (id, username, global_name, avatar_url, email, referral_code, created_at, last_login_at)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?)`, [input.id,
      input.username,
      input.globalName,
      input.avatarUrl,
      input.email,  // ✅ Aggiungi email
      genReferralCode(),
      now(),
      now()]);
    // Default settings row.
    await execute(`INSERT INTO user_settings (user_id, updated_at) VALUES (?, ?) ON CONFLICT (user_id) DO NOTHING`, [input.id, now()]);
  }

  // Backfill a referral code for accounts created before the referral program.
  await usersRepo.ensureReferralCode(input.id);

  return await queryOne("SELECT * FROM users WHERE id = ?", [input.id]) as UserRow;
},

  async byId(id: string): Promise<UserRow | undefined> {
    return await queryOne("SELECT * FROM users WHERE id = ?", [id]) as
      | UserRow
      | undefined;
  },

  /**
   * In this app the user's primary key IS their Discord ID (set at OAuth login),
   * so looking up "by discord id" is the same as looking up by id.
   */
  async byDiscordId(discordId: string): Promise<UserRow | undefined> {
    return usersRepo.byId(discordId);
  },

  async byUsername(username: string): Promise<UserRow | undefined> {
    return await queryOne("SELECT * FROM users WHERE username = ? ORDER BY created_at ASC LIMIT 1", [username]) as UserRow | undefined;
  },

  async markDiscordJoined(id: string): Promise<void> {
    await execute("UPDATE users SET discord_joined = 1 WHERE id = ?", [id]);
  },

  async setAdmin(id: string, isAdmin: boolean): Promise<void> {
    await execute("UPDATE users SET is_admin = ? WHERE id = ?", [isAdmin ? 1 : 0, id]);
  },

  async setBanned(id: string, banned: boolean): Promise<void> {
    await execute("UPDATE users SET is_banned = ? WHERE id = ?", [banned ? 1 : 0, id]);
  },

  async setBeta(id: string, beta: boolean): Promise<void> {
    await execute("UPDATE users SET is_beta = ? WHERE id = ?", [beta ? 1 : 0, id]);
  },

  /** Ensure the user has a unique referral code, generating one if missing. */
  async ensureReferralCode(id: string): Promise<string> {
    const row = (await queryOne("SELECT referral_code FROM users WHERE id = ?", [id])) as
      | { referral_code: string | null }
      | undefined;
    if (row?.referral_code) return row.referral_code;
    // Retry a few times in the (very unlikely) event of a code collision.
    for (let i = 0; i < 5; i++) {
      const code = genReferralCode();
      try {
        await execute("UPDATE users SET referral_code = ? WHERE id = ?", [code, id]);
        return code;
      } catch {
        /* unique violation — try again */
      }
    }
    throw new Error("Could not generate a unique referral code");
  },

  async byReferralCode(code: string): Promise<UserRow | undefined> {
    return (await queryOne("SELECT * FROM users WHERE referral_code = ?", [code])) as
      | UserRow
      | undefined;
  },

  /**
   * Link a new user to their referrer. No-op if the user was already referred,
   * is referring themselves, or the code is invalid.
   */
  async setReferredByCode(userId: string, code: string): Promise<void> {
    const me = await usersRepo.byId(userId);
    if (!me || me.referred_by) return;
    const referrer = await usersRepo.byReferralCode(code);
    if (!referrer || referrer.id === userId) return;
    await execute("UPDATE users SET referred_by = ? WHERE id = ?", [referrer.id, userId]);
  },

  /** Add spendable credit to a user's balance (USD). */
  async addCredit(id: string, amountUsd: number): Promise<void> {
    if (amountUsd === 0) return;
    await execute("UPDATE users SET credit_usd = credit_usd + ? WHERE id = ?", [amountUsd, id]);
  },

  async creditBalance(id: string): Promise<number> {
    const row = (await queryOne("SELECT credit_usd FROM users WHERE id = ?", [id])) as
      | { credit_usd: number }
      | undefined;
    return row?.credit_usd ?? 0;
  },


  /** Read the number of lifetime extra bot slots a user owns. */
  async extraBotSlots(id: string): Promise<number> {
    const row = (await queryOne("SELECT extra_bot_slots FROM users WHERE id = ?", [id])) as
      | { extra_bot_slots: number }
      | undefined;
    return row?.extra_bot_slots ?? 0;
  },

  /** Grant additional lifetime bot slots to a user. */
  async addBotSlots(id: string, qty: number): Promise<void> {
    if (qty === 0) return;
    await execute("UPDATE users SET extra_bot_slots = extra_bot_slots + ? WHERE id = ?", [qty, id]);
  },

  /** Persist the Minecraft username for a user (used so the bot can DM on payment). */
  async setMcUsername(id: string, mcUsername: string): Promise<void> {
    await execute("UPDATE users SET mc_username = ? WHERE id = ?", [mcUsername, id]);
  },

  /** Users referred by `id`, newest first. */
  async referredUsers(id: string): Promise<UserRow[]> {
    return (await query(
      "SELECT * FROM users WHERE referred_by = ? ORDER BY created_at DESC",
      [id],
    )) as UserRow[];
  },

  /**
   * Return the admin's TOTP secret, generating and persisting one on first use.
   * Used to gate critical admin actions behind the website's rotating OTP.
   */
  async getOrCreateAdminOtpSecret(id: string): Promise<string> {
    const row = await queryOne(
      "SELECT admin_otp_secret FROM users WHERE id = ?",
      [id],
    ) as { admin_otp_secret: string | null } | undefined;
    if (row?.admin_otp_secret) return row.admin_otp_secret;
    const secret = generateBase32Secret();
    await execute("UPDATE users SET admin_otp_secret = ? WHERE id = ?", [secret, id]);
    return secret;
  },

  async all(): Promise<UserRow[]> {
    return await query("SELECT * FROM users ORDER BY created_at DESC", []) as UserRow[];
  },

  async count(): Promise<number> {
    return (
      await queryOne("SELECT COUNT(*) AS c FROM users", []) as { c: number }
    ).c;
  },

  /**
   * Permanently delete ALL data associated with a user.
   * Deletes from all related tables first (foreign key safety), then the user.
   * This is irreversible — use with extreme caution.
   */
  async hardDelete(userId: string): Promise<void> {
    // Delete in dependency order (child tables before parent)
    await execute("DELETE FROM bot_hour_balances WHERE user_id = ?", [userId]);
    await execute("DELETE FROM bot_runs WHERE user_id = ?", [userId]);
    await execute("DELETE FROM bots WHERE user_id = ?", [userId]);
    await execute("DELETE FROM payments WHERE user_id = ?", [userId]);
    await execute("DELETE FROM subscriptions WHERE user_id = ?", [userId]);
    await execute("DELETE FROM proxies WHERE assigned_user_id = ?", [userId]);
    await execute("DELETE FROM warns WHERE user_id = ?", [userId]);
    await execute("DELETE FROM notes WHERE user_id = ?", [userId]);
    await execute("DELETE FROM tickets WHERE user_id = ?", [userId]);
    await execute("DELETE FROM notifications WHERE user_id = ?", [userId]);
    await execute("DELETE FROM trial_redemptions WHERE user_id = ?", [userId]);
    await execute("DELETE FROM user_settings WHERE user_id = ?", [userId]);
    await execute("DELETE FROM global_chat_messages WHERE user_id = ?", [userId]);
    await execute("DELETE FROM reviews WHERE user_id = ?", [userId]);
    await execute("DELETE FROM dm_queue WHERE discord_id = ?", [userId]);
    await execute("DELETE FROM user_ips WHERE user_id = ?", [userId]);
    await execute("DELETE FROM admin_audit WHERE actor_id = ? OR target_id = ?", [userId, userId]);
    await execute("DELETE FROM users WHERE id = ?", [userId]);
  },
};

