export const referralCreditsRepo = {
  /**
   * Award referral credit for a paid invoice. Idempotent on payment_id, so
   * re-processing the same payment never double-credits. Also bumps the
   * referrer's spendable balance. Returns the credited amount (0 if skipped).
   */
  async award(input: {
    referrerId: string;
    sourceUserId: string;
    paymentId: string;
    amountUsd: number;
  }): Promise<number> {
    if (input.amountUsd <= 0) return 0;
    const res = await execute(
      `INSERT INTO referral_credits (id, user_id, source_user_id, payment_id, amount_usd, created_at)
       VALUES (?, ?, ?, ?, ?, ?)
       ON CONFLICT (payment_id) DO NOTHING`,
      [newId("rc"), input.referrerId, input.sourceUserId, input.paymentId, input.amountUsd, now()],
    );
    if (res.rowCount === 0) return 0; // already credited
    await usersRepo.addCredit(input.referrerId, input.amountUsd);
    return input.amountUsd;
  },

  async totalEarned(userId: string): Promise<number> {
    const row = (await queryOne(
      "SELECT COALESCE(SUM(amount_usd), 0) AS s FROM referral_credits WHERE user_id = ?",
      [userId],
    )) as { s: number };
    return row.s;
  },

  async historyForUser(userId: string, limit = 20): Promise<ReferralCreditRow[]> {
    return (await query(
      "SELECT * FROM referral_credits WHERE user_id = ? ORDER BY created_at DESC LIMIT ?",
      [userId, limit],
    )) as ReferralCreditRow[];
  },
};

/**
 * Reward the referrer (if any) of the buyer who just completed `payment`.
 * Idempotent on payment id, so it is safe to call from both the crypto webhook
 * and the credit-only instant-paid path. Amount is REFERRAL_RATE of the money
 * actually received (amount_usd, i.e. after any credit was applied).
 */
export async function grantReferralReward(payment: {
  id: string;
  user_id: string;
  amount_usd: number;
}): Promise<void> {
  const buyer = await usersRepo.byId(payment.user_id);
  if (!buyer?.referred_by) return;
  const reward = Math.round(payment.amount_usd * REFERRAL_RATE * 100) / 100;
  if (reward <= 0) return;
  const credited = await referralCreditsRepo.award({
    referrerId: buyer.referred_by,
    sourceUserId: buyer.id,
    paymentId: payment.id,
    amountUsd: reward,
  });
  if (credited <= 0) return; // already credited
  const settings = await settingsRepo.get(buyer.referred_by);
  if (settings.notify_payments !== 0) {
    await notificationsRepo.create({
      userId: buyer.referred_by,
      type: "success",
      title: "Referral reward earned",
      body: `You earned $${credited.toFixed(2)} in account credit from a referral purchase.`,
    });
  }
}

