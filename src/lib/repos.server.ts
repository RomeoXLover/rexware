export const subscriptionsRepo = {
  async create(userId: string, planId: string): Promise<SubscriptionRow> {
    const id = newId("sub");
    await execute(`INSERT INTO subscriptions (id, user_id, plan_id, status, created_at)
       VALUES (?, ?, ?, 'pending', ?)`, [id, userId, planId, now()]);
    return await queryOne("SELECT * FROM subscriptions WHERE id = ?", [id]) as SubscriptionRow;
  },

  async activate(id: string, durationMs: number): Promise<void> {
    const start = now();
    await execute(`UPDATE subscriptions SET status = 'active', started_at = ?, expires_at = ? WHERE id = ?`, [start, start + durationMs, id]);
  },

  async activeForUser(userId: string): Promise<SubscriptionRow | undefined> {
    return await queryOne(`SELECT * FROM subscriptions
         WHERE user_id = ? AND status = 'active' AND (expires_at IS NULL OR expires_at > ?)
         ORDER BY created_at DESC LIMIT 1`, [userId, now()]) as SubscriptionRow | undefined;
  },

  async listForUser(userId: string): Promise<SubscriptionRow[]> {
    return await query("SELECT * FROM subscriptions WHERE user_id = ? ORDER BY created_at DESC", [userId]) as SubscriptionRow[];
  },

  async activeCount(): Promise<number> {
    return (
      await queryOne(`SELECT COUNT(*) AS c FROM subscriptions WHERE status = 'active' AND (expires_at IS NULL OR expires_at > ?)`, [now()]) as { c: number }
    ).c;
  },

  /** Set (or replace) the active plan for a user. Cancels any existing active subs first. */
  async setForUser(userId: string, planId: string, durationDays = 30): Promise<SubscriptionRow> {
    await execute(`UPDATE subscriptions SET status = 'canceled' WHERE user_id = ? AND status = 'active'`, [userId]);
    const id = newId("sub");
    const start = now();
    const expires = start + durationDays * 24 * 60 * 60 * 1000;
    await execute(`INSERT INTO subscriptions (id, user_id, plan_id, status, started_at, expires_at, created_at)
       VALUES (?, ?, ?, 'active', ?, ?, ?)`, [id, userId, planId, start, expires, start]);
    return await queryOne("SELECT * FROM subscriptions WHERE id = ?", [id]) as SubscriptionRow;
  },

  /** Revoke (cancel) all active subscriptions for a user. */
  async revokeForUser(userId: string): Promise<void> {
    await execute(`UPDATE subscriptions SET status = 'canceled' WHERE user_id = ? AND status = 'active'`, [userId]);
  },
};

