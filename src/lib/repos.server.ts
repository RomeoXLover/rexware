export function newId(prefix = ""): string {
  const id = crypto.randomBytes(16).toString("hex");
  return prefix ? `${prefix}_${id}` : id;
}

const now = () => Date.now();

/**
 * Referral reward rate: the referrer earns this fraction of every paid invoice
 * generated by users they referred. 0.15 = 15%.
 */
export const REFERRAL_RATE = 0.15;

/** Length of a referral code (alphanumeric). */
const REFERRAL_CODE_LENGTH = 6;

/** Human-friendly, unambiguous referral code (no 0/O/1/I). */
function genReferralCode(): string {
  const alphabet = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789";
  let out = "";
  const bytes = crypto.randomBytes(REFERRAL_CODE_LENGTH);
  for (let i = 0; i < REFERRAL_CODE_LENGTH; i++) out += alphabet[bytes[i] % alphabet.length];
  return out;
}

